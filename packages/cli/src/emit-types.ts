import { COMPILER_VERSION } from "@s11t/runtime/compiler";
import type { S11tCatalogArtifactV1, S11tCompiledContextV1 } from "@s11t/runtime";

function compareCodeUnits(left: string, right: string): number {
	return left < right ? -1 : left > right ? 1 : 0;
}

function typeFor(variable: S11tCompiledContextV1["variables"][string]): string {
	if (variable.type === "json") return "JsonValue";
	return variable.type;
}

export function emitTypes(artifact: S11tCatalogArtifactV1): string {
	const keys = Object.keys(artifact.contexts).sort(compareCodeUnits);
	const keyUnion = keys.map((key) => `\t| ${JSON.stringify(key)}`).join("\n");
	const values = keys
		.map((key) => {
			const context = artifact.contexts[key]!;
			const fields = Object.keys(context.variables)
				.sort(compareCodeUnits)
				.map((name) => `\t\t${JSON.stringify(name)}: ${typeFor(context.variables[name]!)};`)
				.join("\n");
			return `\t${JSON.stringify(key)}: {${fields.length === 0 ? "" : `\n${fields}\n\t`}};`;
		})
		.join("\n");
	const outputs = keys.map((key) => `\t${JSON.stringify(key)}: "text";`).join("\n");
	return `// Generated by @s11t/cli. Do not edit.\n// compilerVersion: ${COMPILER_VERSION}\n// catalogDigest: ${artifact.catalogDigest}\n\nimport type { CatalogContract, JsonValue } from "@s11t/runtime";\nimport { createCatalog } from "@s11t/runtime";\n\nexport const expectedCatalogDigest = ${JSON.stringify(artifact.catalogDigest)} as const;\n\nexport type SystemContextKey =\n${keyUnion};\n\nexport type SystemContextValueMap = {\n${values}\n};\n\nexport type SystemContextOutputMap = {\n${outputs}\n};\n\nexport type AppCatalogContract = CatalogContract<\n\tSystemContextKey,\n\tSystemContextValueMap,\n\tSystemContextOutputMap\n>;\n\nexport function createAppCatalog(artifact: unknown) {\n\treturn createCatalog<AppCatalogContract>(artifact, { expectedCatalogDigest });\n}\n`;
}
